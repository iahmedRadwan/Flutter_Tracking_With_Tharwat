/// The Question is
/// If you need a variable that can hold any type of value and may change during execution, which data type would you use? Write a code example to show this.

// And Answer is
// dataType that can be accept any dataType value and can change is dynamic dataType

void main() {
  dynamic featchData;
  featchData = "Data";
  print(featchData);
  featchData = 25;
  print(featchData);
}
